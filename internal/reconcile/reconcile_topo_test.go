// Package reconcile internal tests for topo sort and provenance parsing.
// These tests live in the same package (not _test) to access unexported helpers.
package reconcile

import (
	"strings"
	"testing"
)

// ---------------------------------------------------------------------------
// parseRequires
// ---------------------------------------------------------------------------

func TestParseRequires_EmptyUnit(t *testing.T) {
	got := parseRequires("# Generated by OwnBase\n[Container]\nImage=localhost/foo:local\n")
	if len(got) != 0 {
		t.Errorf("expected nil, got %v", got)
	}
}

func TestParseRequires_SingleDep(t *testing.T) {
	content := "# Requires=auth\n[Container]\n"
	got := parseRequires(content)
	if len(got) != 1 || got[0] != "auth" {
		t.Errorf("expected [auth], got %v", got)
	}
}

func TestParseRequires_MultipleDeps(t *testing.T) {
	content := "# Requires=db,auth,cache\n[Container]\n"
	got := parseRequires(content)
	want := []string{"db", "auth", "cache"}
	if len(got) != len(want) {
		t.Fatalf("expected %v, got %v", want, got)
	}
	for i := range want {
		if got[i] != want[i] {
			t.Errorf("index %d: got %q, want %q", i, got[i], want[i])
		}
	}
}

func TestParseRequires_EmptyValue(t *testing.T) {
	got := parseRequires("# Requires=\n[Container]\n")
	if len(got) != 0 {
		t.Errorf("expected nil for empty Requires=, got %v", got)
	}
}


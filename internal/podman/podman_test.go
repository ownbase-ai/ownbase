func TestApplier_RollbackStopsStartedContainer(t *testing.T) {
	a := testApplier(t)
	ensureClean(t, a)

	if err := a.ApplyAction(testStartAction()); err != nil {
		t.Fatalf("start: %v", err)
	}
	if err := a.RollbackAction(testStartAction()); err != nil {
		t.Fatalf("RollbackAction: %v", err)
	}
	waitNotRunning(t, testContainerName, 15*time.Second)
}


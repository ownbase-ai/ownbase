// testImage is pre-pulled by cloud-init; no registry pull needed during tests.
const testImage = "docker.io/library/alpine:latest"

// testContainerName must carry the OwnBase prefix so QueryCurrentState sees it.
const testContainerName = "ownbase-integtest"
const testUnitFilename = testContainerName + ".container"

func testQuadletUnit() string {
	return `# Generated by OwnBase integration tests — do not hand-edit.
[Unit]
Description=OwnBase integration-test container ` + testContainerName + `

[Container]
Image=` + testImage + `
ContainerName=` + testContainerName + `
Exec=sleep infinity

[Service]
Restart=no

[Install]
WantedBy=default.target
`
}

// testApplier returns a podman.Applier pointed at the standard user Quadlet dir.
func testApplier(t *testing.T) *podman.Applier {
	t.Helper()
	home, err := os.UserHomeDir()
	if err != nil {
		t.Fatalf("UserHomeDir: %v", err)
	}
	dir := filepath.Join(home, ".config/containers/systemd")
	if err := os.MkdirAll(dir, 0o755); err != nil {
		t.Fatalf("mkdir quadlet dir: %v", err)
	}
	return &podman.Applier{QuadletDir: dir}
}

func testStartAction() reconcile.PlannedAction {
	return reconcile.PlannedAction{
		Action:       schema.MustNewAction(schema.ActionServiceStart, testContainerName),
		Description:  "start integration-test container",
		Before:       "not running",
		After:        "running",
		UnitFilename: testUnitFilename,
		UnitContent:  testQuadletUnit(),
	}
}

func testStopAction() reconcile.PlannedAction {
	return reconcile.PlannedAction{
		Action:       schema.MustNewAction(schema.ActionServiceStop, testContainerName),
		Description:  "stop integration-test container",
		Before:       "running",
		After:        "not running",
		UnitFilename: testUnitFilename,
	}
}

// ensureClean stops+removes the test container before and after each test.
// It also resets the systemd failed state and removes the podman container
// from storage, which can linger after an interrupted test run (e.g. when
// the test process is killed and ExecStopPost never ran).
func ensureClean(t *testing.T, a *podman.Applier) {
	t.Helper()
	clean := func() {
		_ = a.RollbackAction(testStartAction())
		// Quadlet ExecStop uses SIGTERM (10 s) then SIGKILL (exit 137), which
		// leaves the service in "failed" state. Clear it so the next start is
		// not blocked and so the warning "unit file changed" is suppressed.
		exec.Command("systemctl", "--user", "reset-failed",
			testContainerName+".service").Run()
		// Belt-and-suspenders: remove the container from podman storage in case
		// ExecStopPost did not run (e.g. after an interrupted test run).
		exec.Command("podman", "rm", "-f", testContainerName).Run()
	}
	clean()
	t.Cleanup(clean)
}

// testDesiredOutput builds the minimal compiler.RuntimeOutput for the test
// container (no network, no volume) so Diff can produce the correct plan.
func testDesiredOutput() compiler.RuntimeOutput {
	return compiler.RuntimeOutput{
		QuadletUnits: map[string]string{
			testUnitFilename: testQuadletUnit(),
		},
	}
}

// waitNotRunning polls until the container is no longer running or timeout.
func waitNotRunning(t *testing.T, name string, timeout time.Duration) {
	t.Helper()
	deadline := time.Now().Add(timeout)
	for time.Now().Before(deadline) {
		state, err := runtime.QueryCurrentState()
		if err != nil {
			t.Fatalf("QueryCurrentState: %v", err)
		}
		if !state.RunningContainers[name] {
			return
		}
		time.Sleep(500 * time.Millisecond)
	}
	t.Errorf("container %q still running after %s", name, timeout)
}


// Full reconcile loop — convergence
// ---------------------------------------------------------------------------

func TestReconcileConvergence_StartFromEmpty(t *testing.T) {
	a := testApplier(t)
	ensureClean(t, a)

	plan := reconcile.Plan{Actions: []reconcile.PlannedAction{testStartAction()}}
	mem := &authz.MemAuditLog{}

	if err := reconcile.Apply(plan, authz.NewTrivialCheckpoint(), a, mem); err != nil {
		t.Fatalf("Apply: %v", err)
	}

	for i, r := range mem.Records {
		if r.Outcome != authz.OutcomeApplied {
			t.Errorf("audit record %d: outcome %q, want %q", i, r.Outcome, authz.OutcomeApplied)
		}
	}

	state, err := runtime.QueryCurrentState()
	if err != nil {
		t.Fatalf("QueryCurrentState: %v", err)
	}
	if !state.RunningContainers[testContainerName] {
		t.Errorf("expected %q running after Apply, got: %v", testContainerName, state.RunningContainers)
	}
}

func TestReconcileIdempotency_SecondDiffIsEmpty(t *testing.T) {
	a := testApplier(t)
	ensureClean(t, a)

	plan1 := reconcile.Plan{Actions: []reconcile.PlannedAction{testStartAction()}}
	if err := reconcile.Apply(plan1, authz.NewTrivialCheckpoint(), a, authz.NopAuditLog()); err != nil {
		t.Fatalf("first Apply: %v", err)
	}

	current, err := runtime.QueryCurrentState()
	if err != nil {
		t.Fatalf("QueryCurrentState: %v", err)
	}
	if !current.RunningContainers[testContainerName] {
		t.Fatalf("container not running after first Apply")
	}

	// Scope the re-diff to this test's container only. Other integration test
	// packages (e.g. internal/update) run in parallel and may have their own
	// ownbase-* containers running, which would cause a spurious "stop" action.
	scoped := runtime.FakeCurrentState([]string{testContainerName})
	plan2, err := reconcile.Diff(testDesiredOutput(), scoped, reconcile.DiffOptions{})
	if err != nil {
		t.Fatalf("Diff: %v", err)
	}
	if !plan2.IsEmpty() {
		t.Errorf("expected empty plan on re-diff, got %d action(s):\n%s",
			len(plan2.Actions), reconcile.RenderPlanText(plan2))
	}
}

// ---------------------------------------------------------------------------
// Transactional rollback on mid-plan failure
// ---------------------------------------------------------------------------

func TestReconcile_RollbackOnFailure(t *testing.T) {
	a := testApplier(t)
	ensureClean(t, a)

	// Second action has no unit content — will fail immediately.
	badAction := reconcile.PlannedAction{
		Action:       schema.MustNewAction(schema.ActionServiceStart, "ownbase-bad"),
		UnitFilename: "ownbase-bad.container",
		UnitContent:  "", // triggers "no unit content" error
	}

	plan := reconcile.Plan{Actions: []reconcile.PlannedAction{testStartAction(), badAction}}
	mem := &authz.MemAuditLog{}

	err := reconcile.Apply(plan, authz.NewTrivialCheckpoint(), a, mem)
	if err == nil {
		t.Fatal("expected Apply to fail on the second action")
	}

	// First container must be rolled back.
	waitNotRunning(t, testContainerName, 15*time.Second)

	var hasError, hasRollback bool
	for _, r := range mem.Records {
		if r.Outcome == authz.OutcomeError {
			hasError = true
		}
		if r.Outcome == authz.OutcomeRolledBack {
			hasRollback = true
		}
	}
	if !hasError {
		t.Error("expected 'error' record in audit log")
	}
	if !hasRollback {
		t.Error("expected 'rolled_back' record in audit log")
	}
}

func TestApplier_StartAndStop(t *testing.T) {
	a := testApplier(t)
	ensureClean(t, a)

	if err := a.ApplyAction(testStartAction()); err != nil {
		t.Fatalf("ApplyAction(start): %v", err)
	}

	state, err := runtime.QueryCurrentState()
	if err != nil {
		t.Fatalf("QueryCurrentState after start: %v", err)
	}
	if !state.RunningContainers[testContainerName] {
		t.Errorf("expected %q running, got containers: %v", testContainerName, state.RunningContainers)
	}

	if err := a.ApplyAction(testStopAction()); err != nil {
		t.Fatalf("ApplyAction(stop): %v", err)
	}
	waitNotRunning(t, testContainerName, 15*time.Second)
}


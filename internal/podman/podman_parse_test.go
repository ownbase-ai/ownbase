func TestParseStartProvenance_NoSecretsFileInUnit(t *testing.T) {
	// Units must never contain # SecretsFile= — secrets are discovered by
	// convention, not from unit content.
	unit := "# HealthProbeHTTP=/health\n[Container]\nEnvironment=FOO=bar\n"
	if strings.Contains(unit, "SecretsFile") {
		t.Errorf("unit must not contain SecretsFile annotation")
	}
	hp := parseStartProvenance(unit)
	if hp != "/health" {
		t.Errorf("healthProbeURL: got %q, want %q", hp, "/health")
	}
}


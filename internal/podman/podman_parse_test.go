// ---------------------------------------------------------------------------
// parseBuildProvenance
// ---------------------------------------------------------------------------

func TestParseBuildProvenance_AllFields(t *testing.T) {
	unit := "# BuildSource=services/auth\n# BuildRef=v1.0.0\n# BuildDockerfile=Dockerfile.prod\n# BuildContext=backend\n[Container]\n"
	src, ref, df, ctx := parseBuildProvenance(unit)
	if src != "services/auth" {
		t.Errorf("source: got %q, want %q", src, "services/auth")
	}
	if ref != "v1.0.0" {
		t.Errorf("ref: got %q, want %q", ref, "v1.0.0")
	}
	if df != "Dockerfile.prod" {
		t.Errorf("dockerfile: got %q, want %q", df, "Dockerfile.prod")
	}
	if ctx != "backend" {
		t.Errorf("context: got %q, want %q", ctx, "backend")
	}
}

func TestParseBuildProvenance_ImageBundled(t *testing.T) {
	// Image-bundled units have no BuildSource comment.
	unit := "# Generated by OwnBase\n[Container]\nImage=docker.io/caddy:latest\n"
	src, ref, df, ctx := parseBuildProvenance(unit)
	if src != "" || ref != "" || df != "" || ctx != "" {
		t.Errorf("expected all empty for image-bundled unit, got src=%q ref=%q df=%q ctx=%q",
			src, ref, df, ctx)
	}
}


func newDevTLSCmd() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "dev-tls",
		Short: "Manage local HTTPS simulation (mkcert) for a Base",
		Long: `dev-TLS gives a local Multipass Base real HTTPS using mkcert-signed
certificates (trusted by this machine only) and /etc/hosts entries like
forgejo.mybase.test. Enabled by default on 'ownbasectl create' for local
VMs — see --no-dev-tls / --dev-domain.`,
	}
	cmd.AddCommand(newDevTLSSyncCmd(), newDevTLSTrustCmd())
	return cmd
}

func newDevTLSSyncCmd() *cobra.Command {
	return &cobra.Command{
		Use:   "sync <name>",
		Short: "Refresh /etc/hosts for a dev-TLS Base's current service domains",
		Long: `Reads the Base's live ownbase.yaml over SSH and rewrites this machine's
/etc/hosts entry for it so every service domain (and Forgejo's, if set)
resolves to the Base's current IP. Run this after adding a service with a
new domain:. It does not re-detect the VM's IP after a stop/start — use
'ownbasectl vm start' for that.`,
		Args: cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) error {
			return runDevTLSSync(args[0])
		},
	}
}

func newDevTLSTrustCmd() *cobra.Command {
	return &cobra.Command{
		Use:   "trust",
		Short: "Re-run mkcert -install to (re-)trust the local dev-TLS CA",
		Args:  cobra.NoArgs,
		RunE: func(cmd *cobra.Command, args []string) error {
			return runDevTLSTrust()
		},
	}
}

func runDevTLSTrust() error {
	if !mkcertAvailable() {
		return fmt.Errorf("%s", mkcertInstallHint)
	}
	if err := mkcertEnsureInstalled(); err != nil {
		return err
	}
	fmt.Println("mkcert local CA trusted.")
	return nil
}

func runDevTLSSync(name string) error {
	cfgPath, err := serverconfig.DefaultConfigPath()
	if err != nil {
		return fmt.Errorf("locate config: %w", err)
	}
	cfg, err := serverconfig.Load(cfgPath)
	if err != nil {
		return fmt.Errorf("load config: %w", err)
	}
	profile, err := cfg.ProfileFor(name)
	if err != nil {
		return err
	}
	if profile.DevTLSDomain == "" {
		return fmt.Errorf("Base %q is not using dev-TLS (created with --no-dev-tls, --remote, or mkcert was unavailable) — nothing to sync", name)
	}
	if profile.Host == "" {
		return fmt.Errorf("Base %q has no host recorded", name)
	}

	fmt.Fprintf(os.Stderr, "ownbasectl: reading ownbase.yaml from %q ...\n", name)
	raw, err := tunnel.RunCommand(
		profile.Host, profile.EffectiveSSHUser(), profile.EffectiveSSHKey(),
		"cat /opt/ownbase/checkout/ownbase.yaml", profile.EffectiveSSHPort(),
	)
	if err != nil {
		return fmt.Errorf("read ownbase.yaml from %q over SSH: %w", name, err)
	}

	hostnames, uncovered, err := devTLSHostnamesFromYAML(raw, profile.DevTLSDomain)
	if err != nil {
		return fmt.Errorf("parse ownbase.yaml from %q: %w", name, err)
	}
	if len(uncovered) > 0 {
		fmt.Fprintf(os.Stderr,
			"warning: %s not covered by the *.%s dev-TLS certificate (browsers will show an untrusted-certificate warning) — "+
				"re-run 'ownbasectl create %s --dev-domain <domain>' to reissue for a different base domain\n",
			strings.Join(uncovered, ", "), profile.DevTLSDomain, name)
	}

	if err := writeHostsBlock(name, profile.Host, hostnames); err != nil {
		return err
	}

	if len(hostnames) == 0 {
		fmt.Printf("No domains configured for %q yet — removed its /etc/hosts block.\n", name)
		return nil
	}
	fmt.Printf("Synced %d dev-TLS hostname(s) for %q -> %s:\n", len(hostnames), name, profile.Host)
	for _, h := range hostnames {
		fmt.Printf("  https://%s\n", h)
	}
	return nil
}
